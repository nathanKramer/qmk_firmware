enum {
    M_RIGHT_ELM_ARROW = SAFE_RANGE,
    M_LEFT_ELM_ARROW,
    M_BIG_ARROW,
};

/**
 * Process macros
 */
bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    switch (keycode) {
    case M_RIGHT_ELM_ARROW:
        if (record->event.pressed) {
            // when pressed
            SEND_STRING("|>");
        } else {
            // when released
        }
        break;
    case M_LEFT_ELM_ARROW:
        if (record->event.pressed) {
            SEND_STRING("<|");
        }
        break;
    case M_BIG_ARROW:
        if (record->event.pressed) {
            SEND_STRING("=>");
        }
        break;
    }
    return true;
};


